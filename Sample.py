name="Ethnus"
print("Hello, " + name + "!")
# Output: Hello, Ethnus!

# You can also use an f-string for easier formatting
print(f"Hello, {name}!")
# Output: Hello, Ethnus!

# You can change the value of 'name' to greet someone else
name = "Alice"
print(f"Hello, {name}!")
# Output: Hello, Alice! 

# You can also ask the user for their name
name = input("What is your name? ")
print(f"Hello, {name}!")
# Output will depend on the user's input, for example: Hello, Bob!